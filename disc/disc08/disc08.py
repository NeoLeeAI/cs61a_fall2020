def flip_two(lnk):
    """
    >>> one_lnk = Link(1)
    >>> flip_two(one_lnk)
    >>> one_lnk
    Link(1)
    >>> lnk = Link(1, Link(2, Link(3, Link(4, Link(5)))))
    >>> flip_two(lnk)
    >>> lnk
    Link(2, Link(1, Link(4, Link(3, Link(5)))))
    """
    if lnk.rest is Link.empty:
        pass
    else:
        temp = lnk.first
        lnk.first = lnk.rest.first
        lnk.rest.first = temp
        if lnk.rest.rest:
            flip_two(lnk.rest.rest)

